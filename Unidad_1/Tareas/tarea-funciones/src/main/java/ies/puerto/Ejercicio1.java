package ies.puerto;

/**
 * clase que crae una funcion llamda "areaTriangulo" que calcula el area de
 * triangulo
 * 
 */
public class Ejercicio1 {
    public static void main(String[] args) {

    }

    /**
     * Funcion que realiza el calculo del area de un triangulo
     * 
     * @param base
     * @param altura
     * @return
     */
    public int areaTriangulo(int base, int altura) {
        int resultado = 0;
        resultado = (base * altura) / 2;
        return resultado;
    }

}