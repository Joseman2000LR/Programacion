package ies.puerto.bloque3;

/**
 * Desarrolla un programa que declare una variable de tipo double y otra de tipo
 * float, y luego imprima su diferencia.
 */
public class Ejercicio12 {

    public static void main(String[] args) {
        double numero1 = 236.525;
        float numero2 = 47.5f;
        System.out.println("el numero double " + numero1);
        System.out.println("el numero float " + numero2);
    }
}